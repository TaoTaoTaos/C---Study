// 题目要求：根据斐波那契数列计算sum=2/1+3/2...前20项之和
// 思路：首先，我们需要生成斐波那契数列的前21项（因为我们需要用到第21项作为分母）。然后，我们使用一个循环来计算前20项的和。

#include <stdio.h>

int main()
{
    // 定义斐波那契数列的前21项
    double fib[21];
    fib[0] = 1;
    fib[1] = 2;

    // 生成斐波那契数列的前21项
    for (int i = 2; i < 21; i++)
    {
        fib[i] = fib[i - 1] + fib[i - 2];
    }

    // 计算前20项的和
    double sum = 0;
    for (int i = 1; i < 21; i++)
    {
        sum += fib[i] / fib[i - 1];
    }

    // 输出结果
    printf("前20项之和是：%.2f\n", sum);

    return 0;
}

// 总结：这个题目主要考察了斐波那契数列的生成，以及如何使用循环来计算数列的和。
// 关键注释：fib[i] = fib[i - 1] + fib[i - 2]; 这行代码是生成斐波那契数列的关键，它实现了斐波那契数列的定义。
