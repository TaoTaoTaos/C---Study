/*
题目要求：
求出用1,2,5三个数构成和为100的组合个数。

思路：
1. 用三重循环分别遍历1,2,5的个数。
2. 对于每一种组合，如果1*i + 2*j + 5*k等于100，那么这就是一种有效的组合。
*/

#include <stdio.h>

int main() {
    int count = 0; //! 初始化组合个数为0

    for (int i = 0; i <= 100; i++) //! 遍历1的个数
    { 
        for (int j = 0; j <= 50; j++)//! 遍历2的个数
         { 
            for (int k = 0; k <= 20; k++) //! 遍历5的个数
            { 
                if (1*i + 2*j + 5*k == 100) //! 如果1*i + 2*j + 5*k等于100
                { 
                    count++; //! 组合个数加1
                }
            }
        }
    }

    printf("组合个数：%d\n", count); //! 输出组合个数
    return 0;
}

/*
总结：
本题主要考察了枚举的思想和方法。
关键注释：
1. "for (int i = 0; i <= 100; i++)" 这是遍历1的个数的循环，通过遍历0到100的所有整数，可以找到所有可能的1的个数。
2. "if (1*i + 2*j + 5*k == 100)" 这是判断是否为有效组合的条件，如果1*i + 2*j + 5*k等于100，那么这就是一种有效的组合。
*/