// 循环接收用户输入，
// 直到用户输入"结束"，然后统计并输出输入的正整数个数。

#include <stdio.h>
#include <string.h>

// 定义主函数
int main()
{
    char input[100]; // 定义一个字符数组来存储用户的输入
    int count = 0;   // 定义一个计数器来统计正整数的个数

    printf("请输入信息，输入'结束'以停止：\n"); // 提示用户输入信息

    // 使用无限循环来接收用户的输入，直到用户输入"结束"
    while (1)
    {
        scanf("%s", input); // 接收用户的输入

        //!   使用strcmp函数来检查用户是否输入了"结束"

        if (strcmp(input, "结束") == 0) //! strcmp函数用于比较两个字符串,相同时输出0
        {
            break; // 如果用户输入了"结束"，则跳出循环
        }

        // 使用sscanf函数来检查用户是否输入了一个正整数
        int num;
        if (sscanf(input, "%d", &num) == 1 && num > 0) //! 如果sscanf()成功从input中读取了一个整数，并且这个整数大于0，那么就执行if语句

        //! sscanf函数用于识别一个字符串中某种类型的数据 ，中间写%d表示只接受整数 ，后面&num表示把input的整数赋值给 num
        {
            count++; // 如果用户输入了一个正整数，就增加计数器的值
        }
    }

    // 输出正整数的个数
    printf("输入的正整数个数为：%d\n", count);
    return 0; // 主函数返回0，表示程序正常结束
}