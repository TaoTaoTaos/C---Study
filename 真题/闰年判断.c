/*
题目要求：
判断一个年份是否是闰年。

思路：
1. 如果一个年份能被4整除但不能被100整除，或者能被400整除，那么这个年份就是闰年。
*/

#include <stdio.h>

int main() {
    int year;
    printf("请输入一个年份：");
    scanf("%d", &year); //! 输入一个年份

    if ((year % 4 == 0 && year % 100 != 0) || year % 400 == 0) { //! 判断年份是否是闰年
        printf("%d是闰年\n", year); //! 如果是闰年，输出“是闰年”
    } else {
        printf("%d不是闰年\n", year); //! 如果不是闰年，输出“不是闰年”
    }

    return 0;
}

