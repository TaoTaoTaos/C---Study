// 题目要求：对任意输入的一组无序数据，将其调整为一个非递减数列，并输出。数据由用户自己输入。如果用户输入的数据中包含字母，应当进行错误处理。
// 思路：首先，让用户输入数据的数量，然后依次输入每个数据。在读取数据时，检查每个数据是否为数字。如果不是，跳过该数据并给出错误提示。

#include <stdio.h> // 引入标准输入输出库
#include <ctype.h> // 引入字符处理库

void sort(int arr[], int n)
{ // 定义排序函数，参数为整型数组和数组长度
    for (int i = 0; i < n - 1; i++)
    { // 外层循环，遍历每个元素
        for (int j = 0; j < n - i - 1; j++)
        { // 内层循环，将当前元素与后面的元素进行比较
            if (arr[j] > arr[j + 1])
            {                      // 如果当前元素大于后面的元素
                int temp = arr[j]; // 交换两个元素
                arr[j] = arr[j + 1];
                arr[j + 1] = temp;
            }
        }
    }
}

int main()
{          // 主函数
    int n; // 定义一个整型变量，用于存储数据的数量

    printf("请输入数据的数量："); // 提示用户输入数据的数量
    scanf("%d", &n);              // 读取用户输入的数据数量

    int arr[n];    // 定义一个整型数组，用于存储用户输入的数据
    int count = 0; // 计数器，记录数字的数量

    printf("请输入%d个数据：\n", n); // 提示用户输入数据

    for (int i = 0; i < n; i++)
    {                       // 遍历整型数组
        char input[100];    // 定义一个字符数组，用于存储用户输入的数据
        scanf("%s", input); // 读取用户输入的数据

        if (isdigit(input[0]))
        {                                       // 如果用户输入的数据是数字
            sscanf(input, "%d", &arr[count++]); // 将用户输入的数据转换为整数并存入整型数组
        }
        else
        {                                                     // 如果用户输入的数据不是数字
            printf("错误：'%s' 不是数字，已跳过。\n", input); // 输出错误信息
        }
    }

    sort(arr, count); // 调用排序函数，将数组排序

    printf("排序后的数据为：\n"); // 输出排序后的数据

    for (int i = 0; i < count; i++)
    {                          // 遍历排序后的数组
        printf("%d ", arr[i]); // 输出每个元素
    }

    return 0; // 程序正常结束
}

// 总结：本题主要考察了排序算法的应用和用户输入的处理。通过编写一个排序函数，可以将任意的无序数据调整为非递减数列。同时，通过读取用户输入的数据，并检查每个数据是否为数字，可以有效地处理包含字母的数据。
// 关键注释：本程序中的关键注释是“如果用户输入的数据是数字”，这是处理用户输入的核心步骤，通过检查每个数据是否为数字，可以有效地处理包含字母的数据。
