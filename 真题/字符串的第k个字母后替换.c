//将一字符串的第k个字母开始的全部字符，变成另一个字符的复制，并且用指针完成

#include <stdio.h>  // 引入标准输入输出库
#include <string.h>  // !   引入字符串处理库

// dest和src通常被用作目标（destination）和源（source）的缩写。在我们之前的代码中，dest和src都是字符指针，分别指向目标字符串和源字符串。

//dest：这是一个指向目标字符串的指针。我们将源字符串的内容复制到这个位置。在复制过程中，我们会逐个字符地修改dest所指向的位置，然后将dest向后移动一个位置，以便复制下一个字符。

//src：这是一个指向源字符串的指针。我们从这个位置复制字符到目标字符串。在复制过程中，我们会逐个字符地读取src所指向的位置，然后将src向后移动一个位置，以便读取下一个字符。

void copyStr(char* dest, char* src, int k) {
    src = src + k;  
    //!数组的名字就是数组的首地址。
    // !将源字符串的指针向后移动k个位置
    while(*src != '\0') {  // 当源字符串的当前字符不是字符串结束符时

        *dest = *src;    // 将源字符串的当前字符复制到目标字符串
        //! *dest 和 *src 意思是 指向源字符串的当前字符。 
      
        dest++;  // 将目标字符串的指针向后移动一个位置
        src++;  // 将源字符串的指针向后移动一个位置
    }
    *dest = '\0';  //! 在目标字符串的末尾添加字符串结束符
}

int main() {
    char str1[100] = "Hello, world!";  // 定义源字符串
    char str2[100];  // 定义目标字符串
    int k = 7;  // 定义开始复制的位置

    copyStr(str2, str1, k);  // 调用函数，将str1中从第k个字符开始的所有字符复制到str2中

    printf("复制后的字符串是：%s\n", str2);  // 打印复制后的字符串

    return 0;
}
