/*
题目要求：
输出斐波那契数列。

思路：
1. 初始化前两个斐波那契数为0和1。
2. 从第三个数开始，每个斐波那契数都是前两个斐波那契数的和。
*/

#include <stdio.h>

int main() {
    int f1 = 0, f2 = 1; // 初始化前两个斐波那契数为0和1
    printf("%d %d ", f1, f2); // 输出前两个斐波那契数

    for (int i = 3; i <= 20; i++) { //! 从第三个数开始，输出第20个数
        int f = f1 + f2; //!      每个斐波那契数都是前两个斐波那契数的和

        printf("%d ", f); // 输出斐波那契数

        //! 更新前两个斐波那契数

        f1 = f2;
        f2 = f;
    }

    printf("\n"); //! 输出换行符

    return 0;
}

