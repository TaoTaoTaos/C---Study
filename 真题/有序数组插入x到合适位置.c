#include <stdio.h>

// 题目要求：编写程序实现在一个按照升序排序的数组中查找x应插入的位置，
// 并将x插入数组中，使得数组元素仍按照升序排列，输出插入后的数组。
// 原数组和x值由键盘输入，插入操作用函数实现。

// 思路：首先，我们需要一个函数来找到插入的位置。我们可以通过遍历数组，
// 找到第一个大于x的元素的位置，这就是x应该插入的位置。
// 然后，我们需要一个函数来插入元素。我们可以通过将数组中从插入位置开始的所有元素向后移动一位，
// 然后在插入位置放入x，从而实现插入操作。

// 找到插入位置的函数
int findInsertPosition(int arr[], int n, int x)
{
    // 遍历数组
    for (int i = 0; i < n; i++)
    {
        // 找到第一个大于x的元素的位置
        if (arr[i] > x)
        {
            return i;
        }
    }
    // 如果没有找到大于x的元素，说明x应该插入在数组的末尾
    return n;
}

// 插入元素的函数
void insert(int arr[], int *n, int pos, int x)
{
    // 将数组中从插入位置开始的所有元素向后移动一位
    for (int i = *n; i > pos; i--)
    {
        arr[i] = arr[i - 1];
    }
    // 在插入位置放入x
    arr[pos] = x;
    // 数组长度加1
    (*n)++;
}

int main()
{
    int arr[100], n, x;

    // 输入数组长度
    printf("请输入数组长度：");
    scanf("%d", &n);

    // 输入数组元素
    printf("请输入数组元素：");
    for (int i = 0; i < n; i++)
    {
        scanf("%d", &arr[i]);
    }

    // 输入x值
    printf("请输入x值：");
    scanf("%d", &x);

    // 找到插入位置
    int pos = findInsertPosition(arr, n, x);

    // 插入元素
    insert(arr, &n, pos, x);

    // 输出插入后的数组
    printf("插入后的数组为：");
    for (int i = 0; i < n; i++)
    {
        printf("%d ", arr[i]);
    }

    return 0;
}

// 总结：本题主要考察了数组的插入操作，以及如何在有序数组中找到插入位置。
// 解决该问题的关键在于理解如何通过遍历数组找到插入位置，以及如何通过移动元素来插入新元素。
// 这两个操作都需要对数组的操作有深入的理解。
