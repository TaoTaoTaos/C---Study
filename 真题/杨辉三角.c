//在这个程序中，我们使用一个二维数组来存储杨辉三角的每一行。


#include <stdio.h>

#define N 10  // 定义杨辉三角的行数

int main() {
    int arr[N][N];  // 定义一个二维数组来存储杨辉三角

    // 使用两个嵌套的for循环来生成杨辉三角的每一行
    for(int i = 0; i < N; i++) {
        for(int j = 0; j <= i; j++) {
            // 杨辉三角的第一列和对角线上的元素都是1
            if(j == 0 || j == i) {
                arr[i][j] = 1;
            } else {
                // 其他位置的元素等于其正上方的元素和左上方的元素之和
                arr[i][j] = arr[i - 1][j - 1] + arr[i - 1][j];
            }
            // 打印当前元素
            printf("%d ", arr[i][j]);
        }
        // 打印换行符，以便于打印下一行
        printf("\n");
    }

    return 0;  // 程序正常结束
}

/*在这个程序中，我们首先定义了一个二维数组arr来存储杨辉三角。
然后，我们使用两个嵌套的for循环来生成杨辉三角的每一行。
在每一行中，我们首先设置第一列和对角线上的元素为1，
然后计算其他位置的元素，这些元素是其正上方的元素和左上方的元素之和。
最后，我们打印出整个杨辉三角。*/