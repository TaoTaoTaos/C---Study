#include <stdio.h>
//这个问题是要求出用1,2,5三个数构成和为100的组合个数。我们可以把这个问题想象成有一个目标金额100，我们有1元、2元和5元三种面额的硬币，问有多少种方式可以凑齐这个目标金额。

//解决这个问题的一个有效方法是使用动态规划。动态规划是一种解决问题的方法，它将一个大问题分解成许多小问题，并存储小问题的解，以便在解决大问题时可以重复使用这些解。

//在这个问题中，我们使用一个数组dp来存储每个可能的目标金额的组合个数。例如，dp[i]表示目标金额为i的组合个数。我们首先初始化dp[0]为1，表示目标金额为0的组合只有一种，就是不取任何硬币。
int main() {
    int dp[101] = {0};  // 初始化动态规划数组
    int coins[3] = {1, 2, 5};  // 硬币的面值
    int i, j;

    dp[0] = 1;  // 和为0的组合只有一种，就是不取任何硬币

    // 动态规划过程
    for(i = 0; i < 3; i++) //! 每次循环 1,2,5元硬币都掏出来看看
    {
        for(j = coins[i]; j <= 100; j++) //!  j就表示掏硬币 的面额
         {
            dp[j]  =  dp[j] + dp[j - coins[i]]; //!  dp[j]表示目标金额为 i 的组合个数。
        }
    }

    printf("用1,2,5三个数构成和为100的组合个数为：%d\n", dp[100]);

    return 0;
}
