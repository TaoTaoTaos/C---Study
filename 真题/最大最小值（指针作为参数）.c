#include <stdio.h>
#define N 10

// 定义求最大最小值函数
void maxmin(int arr[], int *pt1, int *pt2, int n)
{                         // 定义函数，参数为数组，两个指针和数组长度
    *pt1 = *pt2 = arr[0]; // 将数组的第一个元素赋值给最大值和最小值
    for (int i = 1; i < n; i++)
    { // 遍历数组
        if (arr[i] > *pt1)
        {                  // 如果当前元素大于最大值
            *pt1 = arr[i]; // 更新最大值
        }
        if (arr[i] < *pt2)
        {                  // 如果当前元素小于最小值
            *pt2 = arr[i]; // 更新最小值
        }
    }
}

int main()
{
    int array[N] = {10, 7, 19, 29, 4, 0, 7, 35, 6, 21}; // 定义数组
    int *p1, *p2, a, b;                                 // 定义两个指针和两个整数变量
    p1 = &a;                                            // 将a的地址赋值给p1
    p2 = &b;                                            // 将b的地址赋值给p2
    maxmin(array, p1, p2, N);                           // 调用函数，求最大最小值
    printf("max=%d,min=%d\n", a, b);                    // 打印最大值和最小值
    return 0;                                           // 返回0，表示程序正常结束
}

//! 关键注释：
//! 1. 这个问题主要考察的是如何使用指针作为函数参数，以及如何在函数中修改指针所指向的值。在C语言中，如果我们想在函数中修改变量的值，我们需要将变量的地址（即指针）传递给函数，然后在函数中通过指针来修改变量的值。
//! 2. 在这个问题中，我们需要找到数组中的最大值和最小值。我们可以通过遍历数组，然后比较每个元素和当前的最大值和最小值，如果当前元素大于最大值，就更新最大值，如果当前元素小于最小值，就更新最小值。
//! 3. 注意，我们需要先将最大值和最小值初始化为数组的第一个元素，然后从数组的第二个元素开始遍历。这是因为如果我们将最大值和最小值初始化为0，那么对于所有元素都大于0的数组，我们将无法找到正确的最小值。
//! 4. 在main函数中，我们首先定义了两个指针p1和p2，然后将变量a和b的地址分别赋值给p1和p2。然后我们调用maxmin函数，将数组，p1，p2和数组长度作为参数传递。在maxmin函数中，我们通过p1和p2修改了a和b的值。最后，我们打印出a和b的值，即数组的最大值和最小值。
