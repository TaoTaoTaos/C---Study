// 题目要求：计算1-1000的奇数和偶数的和
// 思路：我们可以通过一个for循环遍历1到1000，然后通过判断每个数字是奇数还是偶数，然后分别累加到对应的变量中。

#include <stdio.h>

int main() { // 主函数
    int odd_sum = 0; // 初始化奇数和为0
    int even_sum = 0; // 初始化偶数和为0

    for (int i = 1; i <= 1000; i++) { // 遍历1到1000
        if (i % 2 == 0) { // 如果i是偶数
            even_sum += i; // 累加到偶数和
        } else { // 否则
            odd_sum += i; // 累加到奇数和
        }
    }

    printf("Odd sum: %d\n", odd_sum); // 打印奇数和
    printf("Even sum: %d\n", even_sum); // 打印偶数和

    return 0; // 返回0，表示程序正常结束
}

//! 关键注释：
//! 1. 这个问题主要考察的是基本的循环和条件判断语句的使用。我们需要遍历1到1000，然后对每个数字进行判断，如果是奇数就加到奇数和中，如果是偶数就加到偶数和中。
//! 2. 判断一个数字是奇数还是偶数的方法是看它除以2的余数，如果余数为0，那么它就是偶数，否则就是奇数。这是因为偶数可以被2整除，而奇数除以2会有余数。
//! 3. 在C语言中，我们可以使用%运算符来获取一个数字除以另一个数字的余数。例如，i % 2就可以得到i除以2的余数。
//! 4. 这个问题也考察了变量的使用和赋值。我们需要创建两个变量来分别存储奇数和和偶数和，然后在循环中不断更新这两个变量的值。
